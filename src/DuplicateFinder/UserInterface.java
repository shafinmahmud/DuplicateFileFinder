    private void buttonClicked(java.awt.event.MouseEvent evt)//GEN-FIRST:event_buttonClicked
    {//GEN-HEADEREND:event_buttonClicked

        if (pathName == null)
        {
            JOptionPane.showMessageDialog(this, "Choose Directory", "WARNING !", JOptionPane.ERROR_MESSAGE);
        } else
        {
            if (AlgoFileSize.isSelected())
            {
                try
                {
                    String directory = pathName;

                    DFinder listFiles = new DFinder(this, pathName, 1);
                    listFiles.start();

                } catch (Exception e)
                {

                }
            } else if (AlgoMD5.isSelected())
            {
                try
                {
                    String directory = pathName;

                    DFinder listFiles = new DFinder(this, pathName, 2);
                    listFiles.start();

                } catch (Exception e)
                {

                }
            } else if (CRC32.isSelected())
            {
                try
                {
                    String directory = pathName;

                    DFinder listFiles = new DFinder(this, pathName, 3);
                    listFiles.start();

                } catch (Exception e)
                {

                }
            } else
            {
                try
                {
                    String directory = pathName;

                    DFinder listFiles = new DFinder(this, pathName, 1);
                    listFiles.start();

                } catch (Exception e)
                {

                }
            }
        }

    }//GEN-LAST:event_buttonClicked

