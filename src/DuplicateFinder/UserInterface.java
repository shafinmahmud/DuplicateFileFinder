    private void OpenDirActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_OpenDirActionPerformed
    {//GEN-HEADEREND:event_OpenDirActionPerformed
        // display file dialog, so user can choose file or directory to open
        JFileChooser fileChose = new JFileChooser();
        fileChose.setFileSelectionMode(JFileChooser.FILES_AND_DIRECTORIES);

        int result = fileChose.showOpenDialog(this);

        // if user clicked Cancel button on dialog, return
        if (result == JFileChooser.CANCEL_OPTION)
        {
            return;
        }

        File fileName = fileChose.getSelectedFile(); // get File

        // display error if invalid
        if ((fileName == null) || (fileName.getName().equals("")))
        {
            JOptionPane.showMessageDialog(this, "Invalid Name",
                    "Invalid Name", JOptionPane.ERROR_MESSAGE);
        }
        pathName = fileName.toString();

        this.selectedPath.setText(pathName);
    }//GEN-LAST:event_OpenDirActionPerformed

